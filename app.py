    pdf.cell(200, 10, txt=f"‡∏õ‡∏£‡∏∞‡πÄ‡∏†‡∏ó‡πÄ‡∏´‡∏ï‡∏∏: {row_data['Incident_Type']}", ln=True)
    pdf.cell(200, 10, txt=f"‡∏™‡∏ñ‡∏≤‡∏ô‡∏ó‡∏µ‡πà: {row_data['Location']}", ln=True)
    pdf.multi_cell(0, 10, txt=f"‡∏£‡∏≤‡∏¢‡∏•‡∏∞‡πÄ‡∏≠‡∏µ‡∏¢‡∏î‡πÄ‡∏´‡∏ï‡∏∏: {row_data['Details']}")
