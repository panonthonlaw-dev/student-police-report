def create_pdf(row_data):
    pdf = FPDF()
    pdf.add_page()
    
    # ‡∏û‡∏¢‡∏≤‡∏¢‡∏≤‡∏°‡πÇ‡∏´‡∏•‡∏î‡∏ü‡∏≠‡∏ô‡∏ï‡πå‡πÑ‡∏ó‡∏¢ (‡∏Ñ‡∏∏‡∏ì‡∏Ñ‡∏£‡∏π‡∏ï‡πâ‡∏≠‡∏á‡∏°‡∏µ‡πÑ‡∏ü‡∏•‡πå fonts/THSarabunNew.ttf ‡πÉ‡∏ô GitHub)
    try:
        pdf.add_font('THSarabun', '', 'fonts/THSarabunNew.ttf')
        pdf.set_font('THSarabun', '', 16)
    except:
        pdf.set_font('Arial', '', 12) # ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏ü‡∏≠‡∏ô‡∏ï‡πå‡πÑ‡∏ó‡∏¢‡∏à‡∏∞‡πÉ‡∏ä‡πâ Arial ‡πÅ‡∏ó‡∏ô (‡πÅ‡∏ï‡πà‡∏≠‡πà‡∏≤‡∏ô‡πÑ‡∏ó‡∏¢‡πÑ‡∏°‡πà‡∏≠‡∏≠‡∏Å)

    pdf.cell(200, 10, txt="‡πÉ‡∏ö‡∏™‡∏£‡∏∏‡∏õ‡∏Å‡∏≤‡∏£‡∏î‡∏≥‡πÄ‡∏ô‡∏¥‡∏ô‡∏Å‡∏≤‡∏£ - ‡∏™‡∏≤‡∏£‡∏ß‡∏±‡∏ï‡∏£‡∏ô‡∏±‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏ô", ln=True, align='C')
    pdf.ln(10)
    pdf.cell(200, 10, txt=f"‡πÄ‡∏•‡∏Ç‡∏ó‡∏µ‡πà‡∏£‡∏±‡∏ö‡πÅ‡∏à‡πâ‡∏á: {row_data['Report_ID']}", ln=True)
