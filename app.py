        f"‡∏™‡∏ñ‡∏≤‡∏ô‡∏ó‡∏µ‡πà: {row_data.get('Location', 'N/A')}",
        f"‡∏£‡∏≤‡∏¢‡∏•‡∏∞‡πÄ‡∏≠‡∏µ‡∏¢‡∏î‡πÄ‡∏´‡∏ï‡∏∏: {row_data.get('Details', 'N/A')}",
